#CONTEXT HANDLER - Menyimpan history chat
class ChatContext:
    """
    Menyimpan history percakapan agar chatbot lebih natural.
    Max history = 5 pertukaran pesan terakhir.
    """

    def __init__(self, max_history: int = 5):
        self.history: List[Dict] = []
        self.last_intent: Optional[str] = None
        self.max_history = max_history

    def add_message(self, role: str, message: str, intent: Optional[str] = None):
        self.history.append({
            "role": role,
            "message": message,
            "intent": intent,
            "timestamp": datetime.now().strftime("%H:%M:%S")
        })
        if len(self.history) > self.max_history * 2:
            self.history = self.history[-(self.max_history * 2):]
        if intent:
            self.last_intent = intent

    def reset(self):
        self.history = []
        self.last_intent = None

    def get_history(self) -> List[Dict]:
        return self.history

#SIMPLE NLP - Tokenisasi & Normalisasi Teks
class SimpleNLP:
    """
    NLP sederhana berbasis regex untuk Bahasa Indonesia.
    Normalisasi kata gaul + tokenisasi unigram/bigram/trigram.
    Tidak membutuhkan library NLTK eksternal.
    """

    SLANG_MAP = {
        "gak": "tidak", "ga": "tidak", "ngga": "tidak", "nggak": "tidak",
        "udah": "sudah", "udh": "sudah",
        "gimana": "bagaimana", "gmn": "bagaimana",
        "kenapa": "mengapa", "knp": "mengapa",
        "org": "orang", "yg": "yang", "dgn": "dengan",
        "utk": "untuk", "krn": "karena", "karna": "karena",
        "spt": "seperti", "tp": "tapi", "tpi": "tapi",
        "sy": "saya", "aku": "saya",
        "nutrisi": "gizi", "mkn": "makanan",
        "hrs": "harus",
    }

    @staticmethod
    def normalize(text: str) -> str:
        text = text.lower().strip()
        text = re.sub(r"[^\w\s]", " ", text)
        text = re.sub(r"\s+", " ", text)
        words = text.split()
        words = [SimpleNLP.SLANG_MAP.get(w, w) for w in words]
        return " ".join(words)

    @staticmethod
    def tokenize(text: str) -> List[str]:
        return re.findall(r"\b\w+\b", SimpleNLP.normalize(text))

    @staticmethod
    def get_all_phrases(text: str) -> List[str]:
        """Buat unigram, bigram, trigram, 4-gram untuk keyword matching akurat."""
        tokens = SimpleNLP.tokenize(text)
        phrases = list(tokens)
        for n in range(2, 5):
            for i in range(len(tokens) - n + 1):
                phrases.append(" ".join(tokens[i:i+n]))
        return phrases

