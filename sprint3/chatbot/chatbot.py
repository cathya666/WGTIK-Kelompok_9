#MAIN CHATBOT CLASS
class StuntingChatbot:
    """
    Chatbot edukasi stunting lengkap dengan NLP sederhana + context handling.

    Fitur:
    - Intent classification (weighted keyword matching + n-gram)
    - Tokenisasi & normalisasi berbasis regex
    - Context handling (history chat 5 giliran terakhir)
    - Response generation terstruktur dan informatif
    - Siap diintegrasikan ke FastAPI endpoint /chat
    """

    def __init__(self):
        self.nlp        = SimpleNLP()
        self.classifier = IntentClassifier()
        self.generator  = ResponseGenerator()
        self.context    = ChatContext(max_history=5)
        print("StuntingChatbot berhasil diinisialisasi!")

    def get_response(self, message: str) -> Dict:
        """
        Proses pesan user dan kembalikan respons.

        Args:
            message (str): Pesan dari user

        Returns:
            dict: {
                'response'   : str   - teks jawaban bot,
                'intent'     : str   - intent yang terdeteksi,
                'confidence' : float - tingkat keyakinan (0.0-1.0)
            }
        """
        if not message or not message.strip():
            return {
                "response": "Mohon ketikkan pertanyaan Anda.",
                "intent": "empty",
                "confidence": 0.0
            }
        normalized = self.nlp.normalize(message)
        intent, confidence = self.classifier.classify(normalized, self.context)
        response_text = self.generator.generate(intent)
        self.context.add_message("user", message, intent)
        self.context.add_message("bot",  response_text)
        return {
            "response":   response_text,
            "intent":     intent,
            "confidence": round(confidence, 2)
        }

    def reset_context(self):
        self.context.reset()
        print("Context percakapan telah direset.")

    def get_history(self) -> List[Dict]:
        return self.context.get_history()

